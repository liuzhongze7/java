import time
import winsound

# "生日快乐" 的音调序列
notes = [
    (262, 500), (262, 500), (294, 500), (262, 500), (349, 500), (330, 500), (262, 500), (262, 500),
    (294, 500), (262, 500), (392, 500), (349, 500),
    (262, 500), (262, 500), (524, 500), (440, 500), (349, 500), (330, 500), (294, 500), (466, 500), (466, 500),
    (440, 500), (349, 500), (392, 500), (349, 500)
]

# 按照音调序列播放音乐
for note in notes:
    winsound.Beep(note[0], note[1])
    time.sleep(0.05)
